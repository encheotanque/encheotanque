async function main() {
  const host = process.env.DB_HOST || "129.146.31.117";
  const user = process.env.DB_USER || "root";
  const password = process.env.DB_PASSWORD || "nRGboHgFC7PkD9";
  const database = process.env.DB_NAME || "encheotanque";

  console.log(`Connecting to database ${database} at ${host}...`);
  const conn = await mysql.createConnection({ host, user, password, database });

  try {
    // 1. Fetch all stations with municipio like 'Juiz de Fora' (case insensitive)
    const [rows]: any = await conn.execute(
      "SELECT id_posto, nu_cnpjposto, nm_posto, geo_latitude, geo_longitude FROM tb_postos WHERE LOWER(nm_municipio) = 'juiz de fora'"
    );

    console.log(`Found ${rows.length} gas stations registered in Juiz de Fora.`);

    let updatedCount = 0;
    for (const row of rows) {
      const dbCnpj = row.nu_cnpjposto?.trim() || "";
      const coords = STATIONS_COORDS[dbCnpj];

      if (coords) {
        // Real coordinate pair parsed from the CSV input
        console.log(`Updating station [ID: ${row.id_posto}]: ${row.nm_posto} (CNPJ: ${dbCnpj})`);
        console.log(`  Current: Latitude: ${row.geo_latitude}, Longitude: ${row.geo_longitude}`);
        console.log(`  New:     Latitude: ${coords.lat}, Longitude: ${coords.lng}`);

        await conn.execute(
          "UPDATE tb_postos SET geo_latitude = ?, geo_longitude = ? WHERE id_posto = ?",
          [coords.lat, coords.lng, row.id_posto]
        );
        updatedCount++;
      } else {
        console.log(`[-] Coordinate fallback or mismatch for CNPJ: ${dbCnpj} - ${row.nm_posto}`);
      }
    }

    console.log(`\nSuccess! Updated ${updatedCount} stations in Juiz de Fora with their correct latitudes and longitudes from the CSV.`);
  } catch (error) {
    console.error("Error updating coordinates:", error);
  } finally {
    await conn.end();
  }
}

main().catch(console.error);
